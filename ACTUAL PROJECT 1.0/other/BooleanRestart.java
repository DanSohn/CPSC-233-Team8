package other;

public class BooleanRestart {
    
    /**
     * Condition of whether or not the game should be restarted
     */
    public boolean returned = false;
    
    /**
     * Sets the variable returned to the change
     *
     * @param change The new condition of whether the user wants to restart game or not
     */
    public void setReturned(Boolean change) {
        returned = change;
    }
    
}
